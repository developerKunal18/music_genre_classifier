def text_to_points(text, spacing=12):
    points = []
    x_offset = 0

    for char in text:
        block = FONT.get(char.upper(), FONT["A"])

        for y, row in enumerate(block):
            for x, pixel in enumerate(row):
                if pixel == "█":
                    points.append((x + x_offset, y, 0))

        x_offset += spacing

    return points


def rotate(x, y, z, ax, ay, az):
    y, z = y*math.cos(ax)-z*math.sin(ax), y*math.sin(ax)+z*math.cos(ax)
    x, z = x*math.cos(ay)+z*math.sin(ay),-x*math.sin(ay)+z*math.cos(ay)
    x, y = x*math.cos(az)-y*math.sin(az), x*math.sin(az)+y*math.cos(az)
    return x, y, z


def project(x, y, z, sw=160, sh=60, fov=120, dist=40):
    factor = fov / (dist + z)
    px = int(sw/2 + x * factor)
    py = int(sh/2 + y * factor)
    return px, py


def main():
    name = input("Enter your name: ").upper()
    points = text_to_points(name)

    ax = ay = az = 0

    while True:
        clear()
        frame = [[" " for _ in range(160)] for _ in range(60)]

        for x, y, z in points:
            rx, ry, rz = rotate(x - 20, y - 5, z, ax, ay, az)
            px, py = project(rx, ry, rz)

            if 0 <= px < 160 and 0 <= py < 60:
                frame[py][px] = "█"

        for row in frame:
            print("".join(row))

        ax += 0.03
        ay += 0.02
        az += 0.01
        time.sleep(0.03)


if __name__ == "__main__":
    main()
